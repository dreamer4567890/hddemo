    public static Bitmap createBitmapWithshadow(Bitmap bitmap, float radius,
                                                float dx, float dy, int color, boolean bCenter) {
        Paint vPaint = new Paint(Paint.ANTI_ALIAS_FLAG
                | Paint.FILTER_BITMAP_FLAG);
        Bitmap Rtbitmap = Bitmap.createBitmap(bitmap.getWidth()
                        + (int) Math.abs(dx), bitmap.getHeight() + (int) Math.abs(dy),
                Bitmap.Config.ARGB_8888);
        Canvas canvas = new Canvas(Rtbitmap);

        vPaint.setColor(color);
        vPaint.setShadowLayer(radius, dx, dy, color);
        float x = 0;
        float y = 0;
        if (bCenter) {
            x = Math.abs(dx) / 2;
            y = Math.abs(dy) / 2;
        } else {
            if (dx < 0) {
                x = Math.abs(dx);
            }
            if (dy < 0) {
                y = Math.abs(dy);
            }
        }
        canvas.drawRect(x, y, bitmap.getWidth(), bitmap.getHeight(), vPaint);
        Paint vPaint2 = new Paint(Paint.ANTI_ALIAS_FLAG
                | Paint.FILTER_BITMAP_FLAG);
        canvas.drawBitmap(bitmap, x, y, vPaint2);

        return Rtbitmap;
    }

    public static void playImage(Context context, String picPath) {
        if (StringUtils.isEmpty(picPath)) {
            return;
        }

        File picFile = new File(picPath);
        if (!picFile.exists()) {
            return;
        }

        try {
            Intent intent = new Intent(Intent.ACTION_VIEW);
            intent.setFlags(Intent.FLAG_GRANT_READ_URI_PERMISSION | Intent.FLAG_GRANT_WRITE_URI_PERMISSION);
            intent.setDataAndType(fromFile(context, picFile), "image/*");
            context.startActivity(intent);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }


    /**
     * 计算采样大小
     *
     * @param options   选项
     * @param maxWidth  最大宽度
     * @param maxHeight 最大高度
     * @return 采样大小
     */
    private static int calculateInSampleSize(BitmapFactory.Options options, int maxWidth, int maxHeight) {
        if (maxWidth == 0 || maxHeight == 0) return 1;
        int height = options.outHeight;
        int width = options.outWidth;
        int inSampleSize = 1;
        while ((height >>= 1) >= maxHeight && (width >>= 1) >= maxWidth) {
            inSampleSize <<= 1;
        }
        return inSampleSize;
    }

    /**
     * 获取bitmap
     *
     * @param file 文件
     * @return bitmap
     */
    public static Bitmap getBitmap(File file) {
        if (file == null) return null;

        String filePath = file.getAbsolutePath();
        Bitmap cachedBitmap = BitmapUtils.getBitmapFromCache(filePath);
        if (cachedBitmap != null && !cachedBitmap.isRecycled()) {
            return cachedBitmap;
        }

        InputStream is = null;
        try {
            is = new BufferedInputStream(new FileInputStream(file));

            Bitmap bitmap = BitmapFactory.decodeStream(is);
            // 将bitmap放入缓存, 以备下次使用
            cacheBitmap(filePath, bitmap);

            return bitmap;
        } catch (FileNotFoundException e) {
            Log.e(TAG, "getBitmap exception=" + e.toString());
            return null;
        } finally {
            FileUtils.closeIO(is);
        }
    }

    private static void cacheBitmap(String idStr, Bitmap bitmap) {
        if (TextUtils.isEmpty(idStr)) {
            Log.i(TAG, "cacheBitmap: idStr is null or empty");
            return;
        }

        String md5Name = FileUtils.getFileMD5ToString(idStr);

        BitmapUtils.BitmapCache bitmapCache = BitmapUtils.getBitmapCache();
        if (bitmapCache == null) {
            Log.i(TAG, "cacheBitmap: bitmapCache is null");
            return;
        }

        bitmapCache.updateBitmap(md5Name, bitmap, true);
    }

    /**
     * 获取bitmap
     *
     * @param file      文件
     * @param maxWidth  最大宽度
     * @param maxHeight 最大高度
     * @return bitmap
     */
    public static Bitmap getBitmap(File file, int maxWidth, int maxHeight) {
        if (file == null) return null;

        String filePath = file.getAbsolutePath();
        Bitmap cachedBitmap = BitmapUtils.getBitmapFromCache(filePath);
        if (cachedBitmap != null && !cachedBitmap.isRecycled()) {
            return cachedBitmap;
        }

        InputStream is = null;
        try {
            BitmapFactory.Options options = new BitmapFactory.Options();
            options.inJustDecodeBounds = true;
            is = new BufferedInputStream(new FileInputStream(file));
            BitmapFactory.decodeStream(is, null, options);
            options.inSampleSize = calculateInSampleSize(options, maxWidth, maxHeight);
            options.inJustDecodeBounds = false;

            Bitmap bitmap = BitmapFactory.decodeStream(is, null, options);
            // 将bitmap放入缓存, 以备下次使用
            cacheBitmap(filePath, bitmap);

            return bitmap;
        } catch (FileNotFoundException e) {
            Log.e(TAG, "getBitmap exception=" + e.toString());
            return null;
        } finally {
            FileUtils.closeIO(is);
        }
    }


    public static Bitmap getViewSnapshot(View catchBgView, View targetView, ViewGroup targetParent) {
        catchBgView.setDrawingCacheEnabled(true);
        catchBgView.buildDrawingCache();
        Bitmap bitmap = catchBgView.getDrawingCache();
        Rect rect = new Rect();
        rect.set(targetView.getLeft(), targetView.getTop(), targetView.getRight(), targetView.getBottom());
        rect.offset((int) targetView.getTranslationX(), (int) targetView.getTranslationY());
        ViewGroup vg = (ViewGroup) targetView.getParent();
        while (vg != null && vg != targetParent) {
            rect.offset((int) vg.getX(), (int) vg.getY());
            vg = (ViewGroup) vg.getParent();
        }
        if (vg == targetParent && rect.left < bitmap.getWidth() - 1 && rect.top < bitmap.getHeight() - 1) {
            return Bitmap.createBitmap(bitmap, rect.left, rect.top, Math.min(bitmap.getWidth() - rect.left - 1, rect.width()), Math.min(bitmap.getHeight() - rect.top - 1, rect.height()));
        } else {
            return null;
        }
    }

    //获取和白色混合颜色
    private static int getMixtureWhite(int color) {
        int alpha = Color.alpha(color);
        int red = Color.red(color);
        int green = Color.green(color);
        int blue = Color.blue(color);
        return Color.rgb(getSingleMixtureWhite(red, alpha), getSingleMixtureWhite(green, alpha),
                getSingleMixtureWhite(blue, alpha));
    }

    // 获取单色的混合值
    private static int getSingleMixtureWhite(int color, int alpha) {
        int newColor = color * alpha / 255 + 255 - alpha;
        return newColor > 255 ? 255 : newColor;
    }

    /**
     * bitmap转为base64
     *
     * @param bitmap
     * @return
     */
    public static String bitmapToBase64(Bitmap bitmap) {

        String result = null;
        ByteArrayOutputStream baos = null;
        try {
            if (bitmap != null) {
                baos = new ByteArrayOutputStream();
                bitmap.compress(CompressFormat.JPEG, 100, baos);

                baos.flush();
                baos.close();

                byte[] bitmapBytes = baos.toByteArray();
                result = Base64.encodeToString(bitmapBytes, Base64.DEFAULT);
            }
        } catch (IOException e) {
            e.printStackTrace();
        } finally {
            try {
                if (baos != null) {
                    baos.flush();
                    baos.close();
                }
            } catch (IOException e) {
                e.printStackTrace();
            }
        }
        return result;
    }

